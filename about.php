                           <p>Fires can start suddenly and spread quickly, damaging your home and furniture and putting lives in danger. They are caused in a variety of ways, but there are a few simple hints you can follow to prevent them starting.</p>
                           <p>Keep all fires and heaters well guarded, especially open fires. For fitted or portable heaters with a built in guard, give extra protection by adding a surrounding guard particularly if you have young children or older people in the home. For children, use a nursery guard with side clips that fit into fixed wall brackets.<br>
Keep portable heaters and candles away from furniture and curtains. Position safely where they cannot be knocked over.<br>
Don't dry or air clothes over or near the fire, or the cooker.<br>
Do not smoke in bed.<br>
Many fires start in the kitchen, especially fat fires. Never leave a pan unattended when deep fat frying and watch for overheating. For safer frying use oven chips or a thermostatically controlled deep fat fryer.<br>
If there are children around, keep matches and lighters well out of reach.<br>
Fit approved smoke detectors on each floor. Choose a smoke alarm that is mains operated or one with a long life (ten year) battery.<br>
Plan your escape route. Remember Get out, stay out and call the fire brigade out!</p>
