                           <p>Fires can start suddenly and spread quickly, damaging your home and furniture and putting lives in danger. They are caused in a variety of ways, but there are a few simple hints you can follow to prevent them starting.</p>
                           <p>1. Provide adequate means of escape<br>
                              The first rule of fire management requires sufficient escape routes out of the building, in accordance with its scale and occupancy. The number, size and location of exits are specified in the National Building Code (NBC) 2005, a detailed set of guidelines for constructing, maintaining and operating buildings of all types. Office occupiers must additionally ensure that staircases, stairwells and corridors are well-maintained, ventilated and free of obstacles in order to be effective in an emergency.Open spaces in buildings play a crucial role in fire management. As P.D. Karguppikar, joint chief fire officer of the Mumbai Fire Brigade, remarked after the terrorist attacks on 26/11: “The atrium in the old wing of the Taj (hotel) allowed heat to dissipate, and prevented collateral damage to other floors from the fire on the sixth floor."<br>

                              2. Outline clear pathways to exit doors<br>
                              Getting to exits is as important as providing enough exits. NBC guidelines specify the maximum distance a person must travel in order to access a fire exit, and the importance of photo-luminescent signage to enable evacuation at night. Refuge areas such as terraces are critical for high-rises where people can safely congregate, when asked to leave the building in phases.<br>

                              3. Install smoke detection systems<br>

                              The first few minutes of a fire are crucial in containing it. Automatic fire alarm systems such as smoke and heat detectors are mandatory elements in international building codes, and particularly useful in spotting fires during times when occupancy in the building is low.<br>

                              4. Maintain smoke suppression systems<br>

                              Fire extinguishers are only useful if they work, so check them regularly. High-rise buildings, which are harder to access and evacuate, should consider installing automatic sprinkler systems. The National Fire Protection Association (NFPA), a US-based non-profit body, estimates that automatic suppression systems lower the cost of damage by 60%. Karguppikar endorses their use, admitting that “the fire in one of the rooms on the 18th floor of the Oberoi was extinguished by its sprinkler system and it was an eye-opener for all of us".<br>


                              5. Conduct regular fire drills<br>

                              Preventing panic in an emergency is as important as staying away from flames and fumes. Regular fire drills familiarize people with emergency evacuation methods at little cost. Nominate a fire safety officer in every building to ensure that this becomes standard operating procedure.<br>
                              
                              6. Use flame-retardant materials in interiors<br>

                              Materials used in the interiors can save or endanger lives. The combination of wood, paper and textiles makes workstations highly combustible. Fabrics can be made flame-retardant, however, so that they self-extinguish when lit. An increasing number of companies, especially multinationals, request such fabrics despite their price premium, according to data from Indian office furniture manufacturer BP Ergo. Stringent fire regulations abroad make it necessary for US furniture makers such as Herman Miller to provide only fire-tested fabrics.

                              Doors are also assigned a fire-resistance rating, measuring how long they can remain resistant to excessive temperatures and flames without collapsing. Karguppikar lauds the construction of the fire-treated doors in the Taj, which allowed several rooms to stay insulated for hours despite a raging fire just outside.<br>

                              7. Make your office accessible to firefighters<br>

                              Grilled windows are a widespread urban phenomenon, and Jairaj Phatak, commissioner, Brihanmumbai Municipal Corporation (BMC), wittily observes that “residents who have grills on their windows presume that only thieves are kept out, and not firefighters". Occupants of offices in residential buildings with few exits should be wary of locking themselves into confined spaces.<br>

                              8. Keep the building plans handy<br>

                              The tragedy at the Taj was heightened by the lack of buildings plans to guide rescue agencies. It is imperative to make multiple copies of your building plan available, especially during an emergency.<br>

                              9. Ask the local fire brigade to assess safety<br>

                              Fire departments, for a nominal fee, will independently assess your building’s level of fire safety. Storage of hazardous or inflammable materials, old and unstable structures, inadequate escape routes or electricity overloads are potential death traps that are best assessed by professionals.<br>

                              10. Comply with National Building Code<br>

                              “Green buildings" are in vogue but safe structures are sadly not. Both the Mumbai Fire Brigade and BMC commissioner concede that 80% of buildings likely violate accepted codes of building safety, with ignorance and personal whims leading to illegal modifications after gaining requisite occupancy permission.The Delhi Fire Brigade is legally authorized to seal unsafe structures; its Mumbai counterpart hopes to be similarly empowered soon.</p>
                           
